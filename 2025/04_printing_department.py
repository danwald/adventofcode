def griddy(blob: str) -> list[[str]]:
    rows: list[str] = []
    for row in blob.split():
        rows.append(row.strip())
    return rows


def adj_access(grid: list[[str]], r: int, c: int, load: int = 4) -> bool:
    rows, cols = len(grid), len(grid[0])
    adj_count = 0
    if grid[r][c] == "@":
        for i, j in (
            (-1, -1),
            (-1, 0),
            (-1, 1),
            (0, -1),
            (0, 0),
            (0, 1),
            (1, -1),
            (1, 0),
            (1, 1),
        ):
            ri, cj = r + i, c + j
            if 0 <= ri < rows and 0 <= cj < cols and grid[ri][cj] == "@":
                adj_count += 1
        return adj_count <= load

    return False


def main(*args, **_) -> int:
    grid = griddy(args[0])
    rows, cols = len(grid), len(grid[0])
    count = 0
    for c in range(cols):
        for r in range(rows):
            if adj_access(grid, r, c):
                count += 1
    return count


