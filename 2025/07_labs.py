from enum import StrEnum, auto
from typing import TypeVar
from collections import namedtuple

T = TypeVar("T")
GridRow = list[T]
Grid = list[GridRow]

Point = namedtuple("Point", ["row", "col"])


class Direction(StrEnum):
    UP = auto()
    DOWN = auto()
    LEFT = auto()
    RIGHT = auto()


def get_grid(data: str) -> Grid:
    acc: Grid = []
    for line in data.split():
        acc.append(list(line))
    return acc


def get_point(point: Point, direction: Direction) -> Point:
    match direction:
        case Direction.UP:
            return Point(point.row - 1, point.col)
        case Direction.DOWN:
            return Point(point.row + 1, point.col)
        case Direction.LEFT:
            return Point(point.row, point.col - 1)
        case Direction.RIGHT:
            return Point(point.row, point.col + 1)


def inbounds(grid: Grid, point: Point) -> bool:
    rows, cols, row, col = len(grid), len(grid[0]), point.row, point.col
    return 0 <= row < rows and 0 <= col < cols


def get_value(grid: Grid, point: Point, direction: Direction) -> T | None:
    rel_point = get_point(point, direction)
    if inbounds(grid, rel_point):
        return grid[rel_point.row][rel_point.col]
    return None


def set_value(grid: Grid, point: Point, direction: Direction, val: T) -> bool:
    rel_point = get_point(point, direction)
    if inbounds(grid, rel_point):
        grid[rel_point.row][rel_point.col] = val
        return True
    return False


def splits[T](grid: list[list[T]]) -> int:
    count = 0
    for row in range(len(grid)):
        for col in range(len(grid[row])):
            point = Point(row, col)
            match grid[row][col]:
                case ".":
                    if get_value(grid, point, Direction.UP) == "|":
                        grid[row][col] = "|"
                case "S":
                    grid[row][col] = "|"
                case "^":
                    if get_value(grid, point, Direction.UP) == "|":
                        set_value(grid, point, Direction.LEFT, "|")
                        set_value(grid, point, Direction.RIGHT, "|")
                        count += 1
                case "|":
                    pass
        # print(grid[row])
    # print(count)
    return count


def timelines[T](grid: list[list[T]]) -> int:
    count = 0
    stack: list[Point] = []
    memo: set[Point] = set()
    for row in range(len(grid)):
        for col in range(len(grid[row])):
            if grid[row][col] == "S":
                stack.append(Point(row, col))
                break
    while stack:
        print(f"{count} <> {len(stack)}", end="\r")
        cur: Point = stack.pop()

        if not inbounds(grid, cur):
            count += 1
            continue

        if cur in memo:
            continue
        memo.add(cur)

        match grid[cur.row][cur.col]:
            case "S" | "|":
                stack.append(Point(cur.row + 1, cur.col))
            case ".":
                grid[cur.row][cur.col] = "|"
                stack.append(Point(cur.row + 1, cur.col))
            case "^":
                stack.append(Point(cur.row, cur.col - 1))
                stack.append(Point(cur.row, cur.col + 1))

    print(count)
    # for line in grid:
    #    print("".join(line))
    return count


def main(data: str, split_counts=True) -> int:
    grid = get_grid(data)
    if split_counts:
        return splits(grid)
    else:
        return timelines(grid)


